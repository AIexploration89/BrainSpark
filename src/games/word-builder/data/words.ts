// Get words by category
export function getWordsByCategory(category: WordCategory): Word[] {
  switch (category) {
    case 'animals': return ANIMAL_WORDS;
    case 'colors': return COLOR_WORDS;
    case 'food': return FOOD_WORDS;
    case 'nature': return NATURE_WORDS;
    case 'actions': return ACTION_WORDS;
    case 'objects': return OBJECT_WORDS;
    case 'mixed': return [
      ...ANIMAL_WORDS,
      ...COLOR_WORDS,
      ...FOOD_WORDS,
      ...NATURE_WORDS,
      ...ACTION_WORDS,
      ...OBJECT_WORDS,
    ];
    default: return [];
  }
}

// Get words by difficulty (based on word length)
export function getWordsByDifficulty(words: Word[], minLength: number, maxLength: number): Word[] {
  return words.filter(w => w.word.length >= minLength && w.word.length <= maxLength);
}

// Get random words
export function getRandomWords(words: Word[], count: number): Word[] {
  const shuffled = [...words].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
