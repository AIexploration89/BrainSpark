// Helper functions
export function getPlanetById(id: PlanetId): Planet | undefined {
  return PLANETS.find(p => p.id === id);
}

export function getUnlockedPlanets(totalStars: number): Planet[] {
  return PLANETS.filter(p => (p.unlockRequirement ?? 0) <= totalStars);
}

export function getNextPlanetToUnlock(totalStars: number): Planet | undefined {
  const locked = PLANETS.filter(p => (p.unlockRequirement ?? 0) > totalStars);
  return locked.sort((a, b) => (a.unlockRequirement ?? 0) - (b.unlockRequirement ?? 0))[0];
}

export function getAllFacts(): { planetId: PlanetId; fact: Planet['facts'][0] }[] {
  const allFacts: { planetId: PlanetId; fact: Planet['facts'][0] }[] = [];
  PLANETS.forEach(planet => {
    planet.facts.forEach(fact => {
      allFacts.push({ planetId: planet.id, fact });
    });
  });
  return allFacts;
}
