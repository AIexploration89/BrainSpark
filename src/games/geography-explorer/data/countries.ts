// Helper functions
export function getCountriesByContinent(continent: Continent): Country[] {
  if (continent === 'world') return COUNTRIES;
  return COUNTRIES.filter(c => c.continent === continent);
}

export function getCountryById(id: string): Country | undefined {
  return COUNTRIES.find(c => c.id === id);
}

export function getRandomCountries(count: number, continent?: Continent, exclude?: string[]): Country[] {
  let pool = continent && continent !== 'world'
    ? COUNTRIES.filter(c => c.continent === continent)
    : COUNTRIES;

  if (exclude && exclude.length > 0) {
    pool = pool.filter(c => !exclude.includes(c.id));
  }

  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getCountryCount(continent: Continent): number {
  if (continent === 'world') return COUNTRIES.length;
  return COUNTRIES.filter(c => c.continent === continent).length;
}
