// Helper functions
export function getLandmarksByContinent(continent: Continent): Landmark[] {
  if (continent === 'world') return LANDMARKS;
  return LANDMARKS.filter(l => l.continent === continent);
}

export function getLandmarkById(id: string): Landmark | undefined {
  return LANDMARKS.find(l => l.id === id);
}

export function getRandomLandmarks(count: number, continent?: Continent, exclude?: string[]): Landmark[] {
  let pool = continent && continent !== 'world'
    ? LANDMARKS.filter(l => l.continent === continent)
    : LANDMARKS;

  if (exclude && exclude.length > 0) {
    pool = pool.filter(l => !exclude.includes(l.id));
  }

  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getLandmarkCount(continent: Continent): number {
  if (continent === 'world') return LANDMARKS.length;
  return LANDMARKS.filter(l => l.continent === continent).length;
}
