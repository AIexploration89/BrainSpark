// All levels combined
export const ALL_LEVELS: Level[] = [
  ...chapter1Levels,
  ...chapter2Levels,
  ...chapter3Levels,
];

// Get level by ID
export function getLevelById(id: number): Level | undefined {
  return ALL_LEVELS.find(l => l.id === id);
}

// Get next level
export function getNextLevel(currentId: number): Level | undefined {
  const currentIndex = ALL_LEVELS.findIndex(l => l.id === currentId);
  return ALL_LEVELS[currentIndex + 1];
}

// Get levels by chapter
export function getLevelsByChapter(chapter: number): Level[] {
  return ALL_LEVELS.filter(l => l.chapter === chapter);
}

