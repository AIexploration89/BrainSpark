import type { Level, Tile } from '../types';

// Helper to create tiles from a string grid
function createTilesFromGrid(grid: string[], legend: Record<string, Tile['type']>): Tile[] {
  const tiles: Tile[] = [];
  grid.forEach((row, rowIndex) => {
    row.split('').forEach((char, colIndex) => {
      const type = legend[char] || 'floor';
      tiles.push({
        id: `${rowIndex}-${colIndex}`,
        type,
        position: { row: rowIndex, col: colIndex },
      });
    });
  });
  return tiles;
}

