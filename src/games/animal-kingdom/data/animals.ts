// Helper functions
export function getAnimalsByCategory(category: AnimalCategory): Animal[] {
  return ANIMALS.filter(a => a.category === category);
}

export function getAnimalsByHabitat(habitat: Habitat): Animal[] {
  return ANIMALS.filter(a => a.habitat === habitat);
}

export function getAnimalById(id: string): Animal | undefined {
  return ANIMALS.find(a => a.id === id);
}

export function getRandomAnimals(count: number, excludeIds: string[] = []): Animal[] {
  const available = ANIMALS.filter(a => !excludeIds.includes(a.id));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getEndangeredAnimals(): Animal[] {
  return ANIMALS.filter(a => a.isEndangered);
}

export function getNocturnalAnimals(): Animal[] {
  return ANIMALS.filter(a => a.isNocturnal);
}

export function getFlyingAnimals(): Animal[] {
  return ANIMALS.filter(a => a.canFly);
}
